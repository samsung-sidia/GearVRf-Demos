package org.gearvrf.arpet.sharing;

public abstract class SyncTask {

    protected void notifyProcessed() {
        synchronized (this) {
            notify();
        }
    }

    public void start() {
        Thread thread = new Thread(() -> {
            process();
            synchronized (this) {
                try {
                    wait();
                } catch (InterruptedException e) {
                    e.printStackTrace();
                }
            }
        });
        thread.start();
        try {
            thread.join();
        } catch (InterruptedException e) {
            e.printStackTrace();
        }
    }

    public abstract void process();
}
